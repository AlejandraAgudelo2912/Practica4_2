package Ejercicio03;

public class Punto {

	public double x;
	public double y;
	
	public Punto() {
		
	}
	
	public Punto(double x, double y) {
		setX(x);
		setY(y);
	}
	
	public double getX() {
		return this.x;
	}
	
	public void setX(double x) {
			this.x=x;
		
	}
	
	public double getY() {
		return this.y;
	}
	
	public void setY(double y) {
			this.y=y;
		
	}
	
}
